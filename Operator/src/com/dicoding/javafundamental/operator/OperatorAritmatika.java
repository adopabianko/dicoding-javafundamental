package com.dicoding.javafundamental.operator;

public class OperatorAritmatika {
    public static void main(String[] args) {
        System.out.println("Operasi Penjumlahan");
        int hasilPenjumlahan = 5 + 1;
        System.out.println("Hasil dari 5 + 1 = " + hasilPenjumlahan);
        System.out.println();

        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 4 - 1;
        System.out.println("Hasil dari 4 - 1 = " + hasilPengurangan);
        System.out.println();

        System.out.println("Hasil dari perkalian");
        int hasilPerkalian = 5 * 5;
        System.out.println("Hasil dari 5 * 5 = " + hasilPerkalian);
        System.out.println();

        System.out.println("Hasil dari pembagian");
        int hasilPembagian = 20 / 2;
        System.out.println("Hasil dari 20 / 2 = " + hasilPembagian);
        System.out.println();

        System.out.println("Operasi hasil bagi");
        int hasilSisa = 8 % 2;
        System.out.println("Hasil 8 % 2 = " + hasilSisa);
        System.out.println();

        int hasilSisa2 = 9 % 2;
        System.out.println("Hasil 9 % 2 = " + hasilSisa2);
    }
}
